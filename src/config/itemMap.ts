// Helper function to get item type from item name
export function getItemType(itemName: string): 'skins' | 'knives' | 'gloves' | 'agents' | 'unknown' {
  const lowerName = itemName.toLowerCase();
  
  if (lowerName.includes('knife') || lowerName.includes('karambit') || lowerName.includes('bayonet') || 
      lowerName.includes('butterfly') || lowerName.includes('huntsman') || lowerName.includes('flip') ||
      lowerName.includes('gut') || lowerName.includes('bowie') || lowerName.includes('falchion') ||
      lowerName.includes('shadow') || lowerName.includes('navaja') || lowerName.includes('stiletto') ||
      lowerName.includes('talon') || lowerName.includes('ursus') || lowerName.includes('classic') ||
      lowerName.includes('nomad') || lowerName.includes('skeleton') || lowerName.includes('survival') ||
      lowerName.includes('paracord')) {
    return 'knives';
  }
  
  if (lowerName.includes('gloves') || lowerName.includes('hand wraps') || lowerName.includes('moto') ||
      lowerName.includes('driver') || lowerName.includes('specialist') || lowerName.includes('bloodhound') ||
      lowerName.includes('sport')) {
    return 'gloves';
  }
  
  if (lowerName.includes('seal team') || lowerName.includes('fbi') || lowerName.includes('gign') ||
      lowerName.includes('idf') || lowerName.includes('leet krew') || lowerName.includes('sas') ||
      lowerName.includes('swat') || lowerName.includes('soldier') || lowerName.includes('sniper') ||
      lowerName.includes('agent') || lowerName.includes('lieutenant') || lowerName.includes('sergeant') ||
      lowerName.includes('colonel') || lowerName.includes('commander') || lowerName.includes('officer')) {
    return 'agents';
  }
  
  return 'skins';
}
